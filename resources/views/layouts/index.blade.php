        <script>
            document.addEventListener("DOMContentLoaded", function() {

                // ================================
                // 1. COPY MAIN MENU TO STICKY MENU
                // ================================
                let mainMenu = document.querySelector(".main-header .navigation");
                let stickyMenuContainer = document.querySelector(".sticky-header .main-menu");

                if (mainMenu && stickyMenuContainer) {
                    stickyMenuContainer.innerHTML = "<ul class='navigation'>" + mainMenu.innerHTML + "</ul>";
                }

                // =================================
                // 2. COPY MAIN MENU TO MOBILE MENU
                // =================================
                let mobileMenuOuter = document.querySelector(".mobile-menu .menu-outer");

                if (mainMenu && mobileMenuOuter) {
                    mobileMenuOuter.innerHTML = "<ul class='navigation'>" + mainMenu.innerHTML + "</ul>";
                }

                // =================================
                // 3. MOBILE MENU TOGGLER
                // =================================
                const mobileMenu = document.querySelector(".mobile-menu");
                const mobileToggler = document.querySelector(".mobile-nav-toggler");
                const mobileClose = document.querySelector(".mobile-menu .close-btn");

                if (mobileToggler) {
                    mobileToggler.addEventListener("click", () => {
                        mobileMenu.classList.add("active");
                    });
                }

                if (mobileClose) {
                    mobileClose.addEventListener("click", () => {
                        mobileMenu.classList.remove("active");
                    });
                }

            });

        </script>
